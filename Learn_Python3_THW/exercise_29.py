people = 20
emus = 30
koalas = 15

if people < emus:
    print("Too many emus ! Australia is doomed!")

if people > emus:
    print("Not that many emus! Australia is OK")

if people < koalas:
    print("Australia is overrun by koalas")

if people > koalas:
    print("Australia is not overrun by koalas")

koalas += 5

if people >= koalas:
    print("People are greater than or equal to koalas")

if people <= koalas:
    print("People are less than or equal to koalas")

if people == koalas:
    print("People are koalas")